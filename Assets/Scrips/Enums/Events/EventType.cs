public enum EventType {
    // Події, що стосуються бою
    BATTLE_START,
    BATTLE_END,
    BATTLE_PAUSE,  // Кнопка для призупинення бою
    BATTLE_RESUME, // Відновлення бою після паузи
    ON_TURN_START,
    ON_TURN_END,

    // Події карт
    ON_CARD_BATTLE,  // Картка вступила в бій
    ON_CARD_DRAWN,   // Картка була витягнута
    ON_CARD_DISCARDED, // Картка була відкинута
    ON_CARD_PLAY,     // Картка була зіграна
    ON_CARD_DIE,      // Картка була знищена
    ON_CARD_CLICKED,  // Картка була клікнута (для взаємодії з користувачем)

    ON_CREATURE_SUMMONED,
    ON_CREATURE_MOVED,
    ON_CREATURE_ATTACK,
    ON_CREATURE_HURT,
    ON_CREATURE_DEATH,

    // Події для взаємодії з іншими гравцями або для контролю гри
    ON_ENCOUNTER,       // Початок зустрічі з новим супротивником або новою ситуацією
    ON_PLAYER_WIN,      // Гравець виграв
    ON_PLAYER_LOSE,     // Гравець програв
    ON_PLAYER_DRAW,     // Нічия (якщо є така опція)

    // Події, пов'язані з ефектами карт
    ON_EFFECT_APPLIED,    // Застосовано ефект картки
    ON_EFFECT_REMOVED,    // Ефект картки був видалений (наприклад, знято прокляття або буст)
    ON_CARD_EXHAUSTED,    // Картка була використана та більше не може бути використана цього ходу
    ON_CARD_SHUFFLED,     // Картка була перемішана в колоді

    // Події для управління ресурсами гри
    ON_RESOURCE_GAINED,   // Гравець отримав ресурси (ману, енергію і т.д.)
    ON_RESOURCE_SPENT,    // Гравець витратив ресурси для виконання дії (наприклад, витрата мани)
    ON_DECK_SHUFFLED,     // Колода була перемішана

    // Події для карток, пов'язані зі зміною стану
    ON_CARD_FLIPPED,      // Картка була перевернута (можливо в разі карти з двома станами)
    ON_CARD_UPGRADED,     // Картка була удосконалена або змінилася в результаті ефектів

    ON_GAME_START,        // Початок гри
    ON_GAME_OVER,         // Завершення гри
    ON_PLAYER_TURN_START, // Початок ходу гравця
    ON_PLAYER_TURN_END,   // Кінець ходу гравця
    ON_PLAYER_PASS,       // Гравець пропустив хід
    ON_CARD_REVEALED,     // Картка була показана (можливо в результаті певної здатності чи ефекту)

    ON_TRIGGER_ABILITY,   // Спрацьовує спеціальна здатність картки чи істоти
    ON_COMBO_PLAY,         // Комбо — особливе комбо в грі, коли кілька карт граються разом для додаткового ефекту
    ON_DEBUFF_APPLIED,    // До картки або істоти було застосовано дебафф
    ON_BUFF_APPLIED,      // До картки або істоти було застосовано бафф
    ON_GAME_STATE_SAVED,  // Стан гри був збережений (наприклад, для продовження або тестування)
    ON_GAME_STATE_LOADED, // Стан гри був завантажений з попереднього збереження
    ON_VICTORY_POINTS_GAINED, // Гравець отримав переможні очки
}
